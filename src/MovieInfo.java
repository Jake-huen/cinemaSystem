
public class MovieInfo {//영화정보
	String title;
	String runtime;
	
	MovieInfo(){
		title="";
		runtime="";
	}
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getRuntime() {
		return runtime;
	}
	public void setRuntime(String runtime) {
		this.runtime = runtime;
	}
}
