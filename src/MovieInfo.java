
public class MovieInfo {//영화정보
	String name;
	String runtime;
	
	MovieInfo(){
		name="";
		runtime="";
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRuntime() {
		return runtime;
	}
	public void setRuntime(String runtime) {
		this.runtime = runtime;
	}
	
}
